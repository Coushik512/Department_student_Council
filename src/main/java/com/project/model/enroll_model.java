package com.project.model;

public class enroll_model {

	private String name;
	private String email;
	private String number;
	
	private String programme;
	private String workshop_intrested;
	private String food;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getNumber() {
		return number;
	}
	public void setNumber(String number) {
		this.number = number;
	}
	
	public String getProgramme() {
		return programme;
	}
	public void setProgramme(String programme) {
		this.programme = programme;
	}
	public String getWorkshop_intrested() {
		return workshop_intrested;
	}
	public void setWorkshop_intrested(String workshop_intrested) {
		this.workshop_intrested = workshop_intrested;
	}
	public String getFood() {
		return food;
	}
	public void setFood(String food) {
		this.food = food;
	}
	

}
